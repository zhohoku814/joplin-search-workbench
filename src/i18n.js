function resolveLocale(settingValue, detectedLocale) {
	const candidate = String(settingValue || 'auto');
	if (candidate === 'zh-CN' || candidate === 'en') return candidate;
	const locale = String(detectedLocale || '').toLowerCase();
	if (locale.startsWith('zh')) return 'zh-CN';
	return 'en';
}

function format(template, vars) {
	return String(template || '').replace(/\{\{\s*([\w.-]+)\s*\}\}/g, (_m, key) => {
		if (!vars || !Object.prototype.hasOwnProperty.call(vars, key)) return '';
		return String(vars[key]);
	});
}

function createUi(settingValue = 'auto', detectedLocale = 'en') {
	const locale = resolveLocale(settingValue, detectedLocale);
	const dict = MESSAGES[locale] || MESSAGES.en;
	const fallback = MESSAGES.en;
	const t = (key, vars) => format(Object.prototype.hasOwnProperty.call(dict, key) ? dict[key] : fallback[key] || key, vars);
	return {
		locale,
		dict,
		t,
		messages: {
			messageSendFailed: t('status.messageSendFailed'),
			postMessageFailed: t('status.postMessageFailed'),
			pleaseEnterQuery: t('status.pleaseEnterQuery'),
			searchRequestSent: t('status.searchRequestSent'),
			reindexRequestSent: t('status.reindexRequestSent'),
		},
	};
}

